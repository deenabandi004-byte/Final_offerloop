/**
 * Normalize URL - adds https:// if protocol is missing
 */
const normalizeUrl = (url: string | undefined): string | undefined => {
  if (!url) return url;
  url = url.trim();
  if (!url.startsWith('http://') && !url.startsWith('https://')) {
    return `https://${url}`;
  }
  return url;
};

